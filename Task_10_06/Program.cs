namespace Task_10_06;

class Program
//Создать Метод ArrayGeneration не возвращающий значения, принимает целое число n
//, выводит наконсольсгенерированный массив размерности n*n.
{
    /// <summary>
    /// Метод для генерации и вывода двумерного массива размером n*n.
    /// </summary>
    /// <param name="n">Размерность массива.</param>
    public static void ArrayGeneration(int n)
    {
        // Проверка, чтобы n > 0
        if (n <= 0)
        {
            Console.WriteLine("Размер массива должен быть больше 0.");
            return; // Завершаем выполнение метода, если n некорректно
        }

        // Создаем двумерный массив размерностью n*n
        int[,] array = new int[n, n];

        // Генерация рандомных чисел и заполнение массива
        Random random = new Random(); // Инициализация генератора рандомных чисел
        for (int i = 0; i < n; i++) 
        {
            for (int j = 0; j < n; j++) 
            {
                // Заполняем текущую ячейку случайным числом от 0 до 99
                array[i, j] = random.Next(0, 100);
            }
        }

        // Вывод массива на консоль
        Console.WriteLine("Сгенерированный массив:");
        for (int i = 0; i < n; i++) // Проход по строкам массива
        {
            for (int j = 0; j < n; j++) // Проход по столбцам массива
            {
                // Выводим значение с пробелом
                Console.Write(array[i, j] + " ");
            }
            Console.WriteLine(); // Переход на новую строку после завершения
        }
    }

    static void Main(string[] args)
    {
        // Запрос пользователю ввести размерность массива
        Console.Write("Введите размерность массива (n): ");
        int n = Convert.ToInt32(Console.ReadLine());

        // Вызов метода  генерации и вывода массива
        ArrayGeneration(n);
    }
}